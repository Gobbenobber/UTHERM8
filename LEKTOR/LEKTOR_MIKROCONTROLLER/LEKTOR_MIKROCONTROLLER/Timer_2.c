 #include "Timer_2.h"
 #include <avr/io.h>

 void T2Delay(int delay_us) // MAX 15 uS DELAY!!!
 {
	 int us;
	 
	 if (delay_us <= 15 && delay_us > 0) //int delay_ms, skal være mellem 0 og 15
	 {
		 us = delay_us;
	 }
	 
	 else
	 
	 {
		 us = 1;
	 }
	 
	 TCNT2 = (256-(16*us));
	 
	 //Prescaler = 0, normal mode.
	 TCCR2A = 0x00000000;
	 TCCR2B = 0x00000001;

	 //lav ingenting indtil timer 2 flag register sættes
	 while ((TIFR2 & (1<<0)) == 0) {}

	 TCCR2B = 0;

	 TIFR2 = 1<<0;
 }